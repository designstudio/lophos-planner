export function getLocale(language) {
    return LOCALE_BY_LANGUAGE[language] || LOCALE_BY_LANGUAGE.ptBR;
}

export function getAppLanguage(userLanguage) {
    if (userLanguage) return userLanguage;
    if (typeof localStorage !== "undefined" && localStorage.language) return localStorage.language;
    return detectBrowserLanguage();
}

export function t(language, key) {
    const lang = MESSAGES[language] ? language : "ptBR";
    return MESSAGES[lang][key] || MESSAGES.ptBR[key] || key;
}

export function formatDayMonth(date, language, dateFormat) {
    const locale = getLocale(language);
    const parts = new Intl.DateTimeFormat(locale, {
        day: "numeric",
        month: "short",
    }).formatToParts(date);

    const day = parts.find(part => part.type === "day")?.value || "";
    const month = (parts.find(part => part.type === "month")?.value || "").replace(".", "").toLowerCase();

    return dateFormat === "MM-DD" ? `${month} ${day}` : `${day} ${month}`;
}
