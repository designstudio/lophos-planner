export function detectBrowserLanguage() {
    if (typeof navigator === "undefined") return "ptBR";

    const browserLanguage = (navigator.language || navigator.languages?.[0] || "").toLowerCase();
    if (browserLanguage.startsWith("pt")) return "ptBR";
    return "enUS";
}

